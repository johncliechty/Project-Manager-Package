#!/usr/bin/env node
// pm backup b2|r2  — generate rclone config templates and a daily snapshot
// script for Backblaze B2 or Cloudflare R2. The script does NOT ask for
// credentials in chat; the user fills them into ~/.config/rclone/rclone.conf
// after creating the bucket and access key on the provider's website.

import { writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, basename } from "node:path";

const cwd = process.cwd();
const argv = process.argv.slice(2);
const provider = argv[0];

if (!provider || argv.includes("--help") || argv.includes("-h")) {
  console.log(`pm backup b2|r2

Generates an rclone config template and a daily snapshot script for the
project's data/ folder. The user creates the bucket and access key on the
provider's website themselves; the kit never creates accounts.

Examples:
  pm backup b2     # Backblaze B2 — cheapest at scale
  pm backup r2     # Cloudflare R2 — zero egress fees
`);
  process.exit(provider ? 0 : 2);
}

if (!["b2", "r2"].includes(provider)) {
  console.error(`pm backup: provider must be 'b2' or 'r2', got '${provider}'`);
  process.exit(2);
}

const projectName = basename(cwd);
const backupDir = join(cwd, ".pm/backup");
mkdirSync(backupDir, { recursive: true });

const configTemplate = provider === "b2"
  ? `# rclone B2 config — append to ~/.config/rclone/rclone.conf (or %APPDATA%\\rclone\\rclone.conf on Windows)
# After creating a B2 bucket and Application Key on backblaze.com,
# fill the values below and remove this comment block.

[${provider}-${projectName}]
type = b2
account = <YOUR_B2_KEY_ID>
key = <YOUR_B2_APPLICATION_KEY>
hard_delete = true

# (Recommended) Encrypt files client-side before upload:
[${provider}-${projectName}-crypt]
type = crypt
remote = ${provider}-${projectName}:<YOUR_BUCKET_NAME>/${projectName}
password = <YOUR_ENCRYPTION_PASSWORD>           # use \`rclone obscure\`
password2 = <YOUR_SALT>                          # use \`rclone obscure\`
filename_encryption = standard
directory_name_encryption = true
`
  : `# rclone R2 config — append to ~/.config/rclone/rclone.conf (or %APPDATA%\\rclone\\rclone.conf on Windows)
# After creating an R2 bucket and API token on dash.cloudflare.com,
# fill the values below and remove this comment block.

[${provider}-${projectName}]
type = s3
provider = Cloudflare
access_key_id = <YOUR_R2_ACCESS_KEY_ID>
secret_access_key = <YOUR_R2_SECRET_ACCESS_KEY>
endpoint = https://<YOUR_ACCOUNT_ID>.r2.cloudflarestorage.com
acl = private

# (Recommended) Encrypt files client-side before upload:
[${provider}-${projectName}-crypt]
type = crypt
remote = ${provider}-${projectName}:<YOUR_BUCKET_NAME>/${projectName}
password = <YOUR_ENCRYPTION_PASSWORD>           # use \`rclone obscure\`
password2 = <YOUR_SALT>                          # use \`rclone obscure\`
filename_encryption = standard
directory_name_encryption = true
`;

const cfgPath = join(backupDir, "rclone.conf.template");
writeFileSync(cfgPath, configTemplate);

const remoteName = `${provider}-${projectName}-crypt`;
const scriptShell = `#!/usr/bin/env bash
# Daily encrypted snapshot of data/ to ${provider.toUpperCase()}.
# Generated by \`pm backup ${provider}\`. Edit rclone.conf to fill credentials.
set -euo pipefail
cd "$(dirname "$0")/../.."
if ! command -v rclone >/dev/null 2>&1; then
  echo "rclone not installed. Install: https://rclone.org/install/"
  exit 1
fi
TS=$(date +%Y-%m-%d-%H%M)
rclone sync ./data ${remoteName}: \\
  --transfers=4 \\
  --log-file=.pm/backup/last-run.log \\
  --log-level=INFO
echo "Synced to ${remoteName}: at $TS"
`;
const scriptPs1 = `# Daily encrypted snapshot of data/ to ${provider.toUpperCase()}.
# Generated by 'pm backup ${provider}'. Edit rclone.conf to fill credentials.
$ErrorActionPreference = "Stop"
Set-Location (Join-Path $PSScriptRoot "..\\..")
if (-not (Get-Command rclone -ErrorAction SilentlyContinue)) {
  Write-Error "rclone not installed. Install: https://rclone.org/install/"
  exit 1
}
$ts = Get-Date -Format "yyyy-MM-dd-HHmm"
rclone sync .\\data ${remoteName}: \`
  --transfers=4 \`
  --log-file=.pm\\backup\\last-run.log \`
  --log-level=INFO
Write-Host "Synced to ${remoteName}: at $ts"
`;

const scriptShellPath = join(cwd, "bin", "backup-data.sh");
const scriptPs1Path = join(cwd, "bin", "backup-data.ps1");
mkdirSync(join(cwd, "bin"), { recursive: true });
writeFileSync(scriptShellPath, scriptShell);
writeFileSync(scriptPs1Path, scriptPs1);

console.log(`Wrote:
  ${cfgPath}
  ${scriptShellPath}
  ${scriptPs1Path}

Next steps (do these yourself — the kit doesn't):

  1. Create a ${provider === "b2" ? "Backblaze B2" : "Cloudflare R2"} account if you don't have one:
     ${provider === "b2" ? "https://www.backblaze.com/b2/sign-up.html" : "https://dash.cloudflare.com/sign-up"}
  2. Create a bucket. Note the bucket name.
  3. Create an Application Key / API Token scoped to the bucket.
  4. Append the contents of ${cfgPath} to your rclone config:
     - macOS/Linux: ~/.config/rclone/rclone.conf
     - Windows: %APPDATA%\\rclone\\rclone.conf
     and fill in the placeholder values.
  5. Use \`rclone obscure\` to generate obscured passwords for the [crypt] section.
  6. Test: \`rclone lsd ${provider}-${projectName}-crypt:\` should return an empty listing without error.
  7. Run a snapshot manually:
     - macOS/Linux: bash bin/backup-data.sh
     - Windows: powershell -File bin/backup-data.ps1
  8. Schedule it: \`pm windows-task install\` (Windows) or add to crontab (Mac/Linux).
`);
